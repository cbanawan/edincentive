<h2>About Being a Member</h2>
<p>Joining is free and easy, and participation in surveys is simple. To sign up, you will answer some basic questions that will take less than a minute. You will then start receiving invitations to surveys that pay you for your participation. You can set your preference on your profile to control how often you receive invitations and you may cancel or pause your membership at any time.</p>

<p>After you join EdIncentive, you may further personalize your profile to receive invitations to surveys that are more relevant to you. You can complete this step immediately after signing up or at a later date. Filling your profile out completely will usually help you get more opportunities to participate in paid surveys.</p>

<h2>Who We Are</h2>
<p>EdIncentive is a division of Ednak. Ednak was started in 2009 by Colt Alton. The site initially served as a social network for online faculty and later became a trusted source as premier directory of edtech tools. In a true sense of collaboration, the EdIncentive platform is the result of hard working people from around the world.</p>
<h2>Our Mission</h2>
<p>Our mission is to give parents and educators an avenue to raise funds for local schools. We do this by connecting companies seeking input on their products with individuals willing to give their time in exchange for monetary compensation. These rewards can be donated to local schools.</p>
