<h1>About Us</h1>
<img src="/images/about-us.png" class="img-resposive pull-right" style="width: 30%; margin: 20px"/>
<p>EdIncentive is an education company that specializes in helping parents and educators raise funds for local schools. EdIncentive is a division of Ednak, an education technology platform to discover tools & resources through a comprehensive and searchable directory.</p>

<h2>Our History</h2>
<p>In 2015, Ednak began exploring ways to add even more value to the education industry. Ednak had become a valuable platform to discover education tools & resources and it sought to give people interested and involved in education a new way to impact the industry. The result of this effort is EdIncentive.</p>

<p>EdIncentive lets people trade their spare time for spare change for local schools. We offer surveys which can range from questionnaires about products you use every day to purchasing decisions you make. EdIncentive strives to provide a positive survey experience and our members reward local schools based on their effort and opinions.</p>

<h2>How It Works</h2>
<p>We partner with companies, government entities and organizations all over the world that seek input on products that real people use. Each time a company wants to gain new insights from educators, they will send out a survey based on specific profile criteria. We email you an invitation with the estimated length of time and the amount you will be rewarded for completing the survey. </p>
<p>The first step is to sign up and complete your profile. After you have set up your profile, you immediately become eligible to participate in surveys. Each time you complete a survey, you will earn rewards for your time and opinions. These rewards are accumulated on your personal Cashout page. Once you have earned the minimum amount needed, you can cashout your rewards.</p>
